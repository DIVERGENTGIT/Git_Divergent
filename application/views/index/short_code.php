 <h2>Short Code SMS</h2>
 
 <p align="justify">
Short codes are those relatively shorter mobile numbers used for convenience. It is usually a five- digit number to ensure round-the-clock availability of business operations wherein text messages are received from mobile users irrespective of the service provider following which they receive a response within few seconds.
 </p>
 <p align="justify">
 By virtue of their convenience, it becomes easy to remember and is linked with different service providers so that users across the country can avail of the services through a seamless Short code SMS.
 </p>

<p align="justify"><b>What is the need for a Short code SMS? </b></p> 
 
<p align="justify">
 The enormous competition between companies in effectively reaching the targeted 
 customer segment is the basis for short code SMS. The numbers of Mobile users are 
 increasing manifold with each passing day and hence there is a requirement for 
 marketing through mobiles using Short code SMS.
</p>
<p>Attributes</p>
<ul>
<li><p>Automated response</p></li>
<li><p>No limit on the number of sub keywords</p></li>
<li><p>Round-the-clock technical support</p></li> 
<li><p>Application integration by means of SMS APIs</p></li>
<li><p>Accurate reporting and tracking of each and every user enquiry</p></li>
<li><p>Real-time reporting through web</p></li>
</ul>
