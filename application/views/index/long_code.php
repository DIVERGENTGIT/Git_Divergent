 <h2>Long Code SMS</h2>
 
 <p align="justify">
Long code is a virtual and lengthy mobile number used by businesses for the purpose of receiving SMS. Striker Solutions helps businesses in generating a lot of leads through their own numbers when compared to short codes specific for several brands.
 </p>
 <p align="justify">
 SMS Long code service is used chiefly for the purpose of generating leads and eliciting consumer feedback. Any company which avails of this service are provided with a Keyword, which is a like a unique ID for a particular company. The messages that come back are segregated on the basis of keywords and routed to the accounts of specific companies.
 </p>
 
<p align="justify">
 The Long Codes provided by Striker Solutions enable a wide range of industries to generate a number of mobile generated SMS. Due to its low cost, long codes are a good solution for long-term campaigns.
</p>
